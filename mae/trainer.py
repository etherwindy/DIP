    #return {k: meter.global_avg for k, meter in metric_logger.meters.items()}

def mae_trainer(model, train_loader, num_epochs, device, learning_rate, weight_decay, args=None):
    writer = SummaryWriter("pretrainedModel/vit")
    optimizer = torch.optim.AdamW(model.parameters(), lr=learning_rate, weight_decay=weight_decay, betas=(0.9, 0.95))
    loss_scaler = NativeScalerWithGradNormCount()
    
    last = time.time()
    for epoch in range(num_epochs):
        train_one_epoch(model, train_loader, optimizer, device, epoch, loss_scaler, log_writer=writer, args=args)
        cur = time.time()
        print("Epoch time: ", cur - last)
    